public class SalesConsltant {

    private String id; //销售顾问id
    private String name;//销售顾问名字

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return  name;
    }

    @Override
    public boolean equals(Object o) {
        SalesConsltant temp = (SalesConsltant) o;
        if(temp.getName().equals(name) && temp.getId().equals(id)){
            return true;
        }else{
            return false;
        }
        //return super.equals(o);
    }
}
